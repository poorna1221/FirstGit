package com.sparity.shoppingcart.view;


public class ProductTypeViewVO {
	
	private int prodTypeId;
	
	private String prodTypeCode;
	
	private String prodTypeName;
	
	private String prodTypeDesc;
	private String prodTypeImpage;
	
	public int getProdTypeId() {
		return prodTypeId;
	}
	public void setProdTypeId(int prodTypeId) {
		this.prodTypeId = prodTypeId;
	}
	public String getProdTypeCode() {
		return prodTypeCode;
	}
	public void setProdTypeCode(String prodTypeCode) {
		this.prodTypeCode = prodTypeCode;
	}
	public String getProdTypeName() {
		return prodTypeName;
	}
	public void setProdTypeName(String prodTypeName) {
		this.prodTypeName = prodTypeName;
	}
	public String getProdTypeDesc() {
		return prodTypeDesc;
	}
	public void setProdTypeDesc(String prodTypeDesc) {
		this.prodTypeDesc = prodTypeDesc;
	}
	public String getProdTypeImpage() {
		return prodTypeImpage;
	}
	public void setProdTypeImpage(String prodTypeImpage) {
		this.prodTypeImpage = prodTypeImpage;
	}
	

}
